/**
 * 拟人化轨迹生成器
 * 生成类似真实用户操作的鼠标轨迹数据
 */

class HumanizedTrajectoryGenerator {
    constructor() {
        // 常见的HTML元素类型
        this.targetTypes = ['DIV', 'BUTTON', 'INPUT', 'SPAN', 'A', 'svg', 'path', 'IMG'];

        // 动作类型
        this.actionTypes = [
            'mousemove##Puzzle##COMPONENT_ACTION',
            'mousedown##Puzzle##COMPONENT_ACTION',
            'mouseup##Puzzle##COMPONENT_ACTION',
            'click##Puzzle##COMPONENT_ACTION'
        ];

        // 屏幕区域配置
        this.screenConfig = {
            width: 1920,
            height: 1080,
            safeZone: {
                minX: 100,
                maxX: 1820,
                minY: 100,
                maxY: 980
            }
        };
    }

    /**
     * 生成随机数（指定范围内）
     */
    randomBetween(min, max) {
        return Math.floor(Math.random() * (max - min + 1)) + min;
    }

    /**
     * 生成贝塞尔曲线路径点
     * 模拟人类鼠标移动的自然曲线
     */
    generateBezierPath(startX, startY, endX, endY, steps = 20) {
        const path = [];

        // 控制点，增加一些随机性来模拟人类的不完美移动
        const cp1X = startX + (endX - startX) * 0.25 + this.randomBetween(-50, 50);
        const cp1Y = startY + (endY - startY) * 0.25 + this.randomBetween(-30, 30);
        const cp2X = startX + (endX - startX) * 0.75 + this.randomBetween(-50, 50);
        const cp2Y = startY + (endY - startY) * 0.75 + this.randomBetween(-30, 30);

        for (let i = 0; i <= steps; i++) {
            const t = i / steps;
            const x = Math.pow(1 - t, 3) * startX +
                     3 * Math.pow(1 - t, 2) * t * cp1X +
                     3 * (1 - t) * Math.pow(t, 2) * cp2X +
                     Math.pow(t, 3) * endX;
            const y = Math.pow(1 - t, 3) * startY +
                     3 * Math.pow(1 - t, 2) * t * cp1Y +
                     3 * (1 - t) * Math.pow(t, 2) * cp2Y +
                     Math.pow(t, 3) * endY;

            path.push({
                x: Math.round(x),
                y: Math.round(y)
            });
        }

        return path;
    }

    /**
     * 生成拟人化的时间间隔
     * 模拟人类操作的不规律性
     */
    generateHumanizedInterval(baseInterval = 16) {
        // 基础间隔 + 随机波动 + 偶尔的停顿
        const randomFactor = Math.random();
        let interval = baseInterval;

        if (randomFactor < 0.1) {
            // 10% 概率出现较长停顿（思考时间）
            interval += this.randomBetween(50, 200);
        } else if (randomFactor < 0.3) {
            // 20% 概率出现短暂停顿
            interval += this.randomBetween(10, 50);
        } else {
            // 正常的小幅波动
            interval += this.randomBetween(-8, 15);
        }

        return Math.max(interval, 8); // 最小间隔8ms
    }

    /**
     * 根据位置选择合适的目标元素
     */
    selectTargetByPosition(x, y) {
        // 根据位置特征选择可能的元素类型
        if (x < 200 || x > 1720) {
            // 边缘区域更可能是导航元素
            return Math.random() < 0.6 ? 'DIV' : 'BUTTON';
        } else if (y < 150) {
            // 顶部区域更可能是导航栏
            return Math.random() < 0.5 ? 'A' : 'BUTTON';
        } else if (Math.random() < 0.3) {
            // 30% 概率是SVG相关元素（图标等）
            return Math.random() < 0.5 ? 'svg' : 'path';
        } else {
            // 其他常见元素
            return this.targetTypes[this.randomBetween(0, 3)]; // DIV, BUTTON, INPUT, SPAN
        }
    }

    /**
     * 生成单次移动轨迹
     */
    generateMovementTrajectory(startX, startY, endX, endY, startTime = 0) {
        const trajectory = [];
        const path = this.generateBezierPath(startX, startY, endX, endY);
        let currentTime = startTime;

        for (let i = 0; i < path.length; i++) {
            const point = path[i];
            const target = this.selectTargetByPosition(point.x, point.y);

            trajectory.push({
                target: target,
                type: 'mousemove##Puzzle##COMPONENT_ACTION',
                time: currentTime,
                keyType: 208, // 固定值，似乎是某种状态标识
                x: point.x,
                y: point.y
            });

            if (i < path.length - 1) {
                currentTime += this.generateHumanizedInterval();
            }
        }

        return {
            trajectory: trajectory,
            endTime: currentTime
        };
    }

    /**
     * 生成点击操作
     */
    generateClickAction(x, y, time) {
        const target = this.selectTargetByPosition(x, y);
        const actions = [];

        // mousedown
        actions.push({
            target: target,
            type: 'mousedown##Puzzle##COMPONENT_ACTION',
            time: time,
            keyType: 208,
            x: x,
            y: y
        });

        // 短暂停留（点击持续时间）
        const clickDuration = this.randomBetween(80, 150);

        // mouseup
        actions.push({
            target: target,
            type: 'mouseup##Puzzle##COMPONENT_ACTION',
            time: time + clickDuration,
            keyType: 208,
            x: x,
            y: y
        });

        return {
            actions: actions,
            endTime: time + clickDuration
        };
    }

    /**
     * 生成完整的拟人化轨迹
     */
    generateHumanizedTrajectory(options = {}) {
        const {
            duration = 5000,           // 总持续时间（毫秒）
            clickCount = 2,            // 点击次数
            startPosition = null,      // 起始位置
            includeRandomMovements = true  // 是否包含随机移动
        } = options;

        const trajectory = [];
        let currentTime = 0;

        // 确定起始位置
        let currentX = startPosition ? startPosition.x : this.randomBetween(
            this.screenConfig.safeZone.minX,
            this.screenConfig.safeZone.maxX
        );
        let currentY = startPosition ? startPosition.y : this.randomBetween(
            this.screenConfig.safeZone.minY,
            this.screenConfig.safeZone.maxY
        );

        // 生成初始位置
        trajectory.push({
            target: this.selectTargetByPosition(currentX, currentY),
            type: 'mousemove##Puzzle##COMPONENT_ACTION',
            time: currentTime,
            keyType: 208,
            x: currentX,
            y: currentY
        });

        const clickTimes = [];
        for (let i = 0; i < clickCount; i++) {
            clickTimes.push(this.randomBetween(duration * 0.2, duration * 0.8));
        }
        clickTimes.sort((a, b) => a - b);

        let nextClickIndex = 0;

        while (currentTime < duration) {
            // 检查是否需要执行点击
            if (nextClickIndex < clickTimes.length &&
                currentTime >= clickTimes[nextClickIndex]) {

                const clickResult = this.generateClickAction(currentX, currentY, currentTime);
                trajectory.push(...clickResult.actions);
                currentTime = clickResult.endTime;
                nextClickIndex++;

                // 点击后短暂停留
                currentTime += this.randomBetween(100, 300);
                continue;
            }

            // 生成下一个目标位置
            const nextX = this.randomBetween(
                this.screenConfig.safeZone.minX,
                this.screenConfig.safeZone.maxX
            );
            const nextY = this.randomBetween(
                this.screenConfig.safeZone.minY,
                this.screenConfig.safeZone.maxY
            );

            // 生成移动轨迹
            const movementResult = this.generateMovementTrajectory(
                currentX, currentY, nextX, nextY, currentTime
            );

            // 跳过第一个点（避免重复）
            trajectory.push(...movementResult.trajectory.slice(1));

            currentX = nextX;
            currentY = nextY;
            currentTime = movementResult.endTime;

            // 随机停留时间
            if (includeRandomMovements && Math.random() < 0.3) {
                currentTime += this.randomBetween(200, 800);
            }
        }

        return trajectory;
    }

    /**
     * 生成特定模式的轨迹（如拖拽操作）
     */
    generateDragTrajectory(startX, startY, endX, endY, options = {}) {
        const {
            startTime = 0,
            dragSpeed = 'normal'  // slow, normal, fast
        } = options;

        const trajectory = [];
        let currentTime = startTime;

        // 移动到起始位置
        const moveToStart = this.generateMovementTrajectory(
            startX - 50, startY - 50, startX, startY, currentTime
        );
        trajectory.push(...moveToStart.trajectory);
        currentTime = moveToStart.endTime;

        // 鼠标按下
        trajectory.push({
            target: this.selectTargetByPosition(startX, startY),
            type: 'mousedown##Puzzle##COMPONENT_ACTION',
            time: currentTime,
            keyType: 208,
            x: startX,
            y: startY
        });

        currentTime += this.randomBetween(50, 100);

        // 拖拽过程
        const baseInterval = dragSpeed === 'slow' ? 25 : dragSpeed === 'fast' ? 10 : 16;
        const dragPath = this.generateBezierPath(startX, startY, endX, endY, 30);

        for (let i = 1; i < dragPath.length; i++) {
            const point = dragPath[i];
            trajectory.push({
                target: this.selectTargetByPosition(point.x, point.y),
                type: 'mousemove##Puzzle##COMPONENT_ACTION',
                time: currentTime,
                keyType: 208,
                x: point.x,
                y: point.y
            });

            currentTime += this.generateHumanizedInterval(baseInterval);
        }

        // 鼠标释放
        trajectory.push({
            target: this.selectTargetByPosition(endX, endY),
            type: 'mouseup##Puzzle##COMPONENT_ACTION',
            time: currentTime,
            keyType: 208,
            x: endX,
            y: endY
        });

        return trajectory;
    }
}

