function generateTrajectory(options = {}) {
    const generator = new HumanizedTrajectoryGenerator();

    // 默认配置
    const config = {
        duration: 3000,           // 持续时间（毫秒）
        clickCount: 2,            // 点击次数
        startPosition: { x: 800, y: 600 },  // 起始位置
        includeRandomMovements: true,       // 包含随机移动
        ...options  // 覆盖默认配置
    };

    return generator.generateHumanizedTrajectory(config);
}

